    private void CrearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearButtonActionPerformed
        Etapa general = new Etapa("General", (int) GeneralSpinner.getValue());
        Etapa especial = new Etapa("Especial", (int) EspecialSpinner.getValue());
        Etapa competitiva = new Etapa("Competitiva", (int) CompetitivaSpinner.getValue());
        int validacionTotal=general.getTotalSemanas()+especial.getTotalSemanas()+competitiva.getTotalSemanas();
        if(semanasTotal!=validacionTotal){
            JOptionPane.showMessageDialog(this, "La cantidad de semanas no coincide con el total de semanas del regimen, intentelo de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        etapas.add(general);
        etapas.add(especial);
        etapas.add(competitiva);
        DefinirMesociclo mesociclosFrame = new DefinirMesociclo(etapas);
        mesociclosFrame.setVisible(true);
        FrmCrearNuevoRegimen.setEtapasRegimen(etapas);
        super.dispose();
    }//GEN-LAST:event_CrearButtonActionPerformed

